; Cоздание текстового файла
;   30 октября 1996 года
;       Функция 3ch - создание файла
;  вход CX     - атрибут файла
;       DS:DX  - адрес ASCIIZ-строки имени файла
;  выход AX    - логический номер
;      Функция 40h - запись в файл
;  вход BX     - атрибут файла
;       CX     - кол записываемых байт
;       DS:DX  - адрес буфера
;  выход AX    - число записанных байт
;      Функция 3eh - закрытие файла
;  вход BX     - логический номер  файла
;                        Могут возвращаться коды ошибок
dosseg
.model small
.stack 20h
.data
file_name db 'file.txt',0
handle    dw 0
text1     db 10,13,'файл не соэдан','$'
text2     db 10,13,'ВВеди','$'
vv        db '12345678901234567890999$$$$$$$$$'
.code
go:        mov ax,@data
           mov ds,ax
         mov cx,5
nach:         mov ah,40h
         lea dx,vv+2
         int 21h
;         loop nach
          mov ah,4ch
          int 21h
end go

